package arroganteIT.joshua_bloch__book_practice.Builder;

public class Frame {

    private String color;
    private String type;

    public Frame(String color, String type) {
        this.color = color;
        this.type = type;
    }

    public String getColor() {
        return color;
    }

    public String getType() {
        return type;
    }
}
